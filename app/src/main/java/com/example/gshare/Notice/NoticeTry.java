package com.example.gshare.Notice;

public class NoticeTry {

        String name;
        String day;
        String G;
        public NoticeTry(String name,String day,String G){
            this.name = name;
            this.day = day;
            this.G = G;
        }
        public String getName(){
            return name;
        }
        public String getDay(){
            return day;
        }
        public String getG(){
            return G;
        }

}
